function showImage(x){
  if(x == "1"){

    document.querySelector('.image-preview').src = "img/item_1.png"
    document.querySelector('.image-preview').alt = "image 1 preview"

  }
  if(x == "2"){

    document.querySelector('.image-preview').src = "img/item_2.png"
    document.querySelector('.image-preview').alt = "image 2 preview"

  }
  if(x == "3"){

    document.querySelector('.image-preview').src = "img/item_3.png"
    document.querySelector('.image-preview').alt = "image 3 preview"

  }

}
